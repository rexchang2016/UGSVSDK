package com.tencent.qcloud.ugckit.module.followRecord;

/**
 * 合唱视频信息
 */
public class FollowRecordInfo {

    /**
     * 视频录制的路径
     */
    public String recordPath;
    /**
     * 跟拍视频的路径
     */
    public String playPath;
    /**
     * 跟拍视频的时长
     */
    public int duration;
    /**
     * 跟拍视频的视频帧率
     */
    public int fps;
}
